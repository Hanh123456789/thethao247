                  <h1 class="news-name-detail">Chính sách bảo mật thông tin khách hàng</h1>
                  <div class="createdate">08/05/2014 01:26</div>
                  <h2 class="intro-detail"></h2>
                  <div class="content-detail"><p>Chúng tôi rất quan tâm đến quyền riêng tư của quý khách khi quý khách sử dụng những dịch vụ của chúng tôi.Chúng tôi cũng hiểu rằng quý khách sẽ rất quan tâm đến việc những thông tin mà quý khách cũng cấp cho chúng tôi có được bảo mật an toàn hay không. Và chúng tôi luôn muốn quý khách sẽ thật yên tâm và tin tưởng khi tham gia các dịch vụ của chúng tôi. Vì vậy chúng tôi cam kết sẽ khiến quý khách có những trải nghiệm tuyệt vời nhất khi tham gia chương trình của chúng tôi với sự tin tưởng hoàn toàn.</p>

                    <p>Chúng tôi tạo ra chính sách bảo mật này để chứng minh cho cam kết về sự an toàn bảo mật của công ty chúng tôi với quý khách hàng. Qua Chính sách bảo mật thông tin này, chúng tôi muốn quý khách hiểu được về việc chúng tôi thu thập thông tin khách hàng, việc sử dụng và chia sẻ thông tin cũng như việc bảo mật thông tin khách hàng của chúng tôi. Chúng tôi cũng hi vọng quý khách sẽ đọc thật kỹ những điều chúng tôi mô tả dưới đây.</p>

                    <h3>Thu Thập Thông Tin.</h3>

                    <p>- Chúng tôi thu thập thông tin từ tất cả những ai truy cập và sử dụng nội dung dịch vụ của chúng tôi, kể cả những Khách ghé thăm không đăng ký tài khoản nhưng có mua và sử dụng các dịch vụ và những Thành viên của chúng tôi.</p>

                    <p>- Thông tin thanh toán của khách hàng: Chúng tôi sử dụng những thông tin này chủ yếu để cung cấp những thông tin hữu ích nhất được cập nhật hàng ngày cho những khách hàng sử dụng dịch vụ của chúng tôi, và nói chung, chúng tôi không bao giờ chia sẻ những thông tin thu được này cho bất cứ bên thứ ba nào.</p>

                    <p>- Chúng tôi sẽ lưu trữ toàn bộ những thông tin truy cập cũng như phản hồi của khách hàng. Những thông tin này được lưu trữ nhằm mục đích chăm sóc tốt hơn cho những khách hàng của chúng tôi.</p>

                    <p>- Chúng tôi sẽ lưu trữ tất cả những hóa đơn chứng từ mua bán, giao dịch, lịch sử mua hàng, phương thức thanh toán … nhằm tránh những phát sinh đáng tiếc nếu có sau này.</p>

                    <h3>Sử Dụng Và Chia Sẻ Thông Tin.</h3>

                    <p><strong>Sử Dụng Thông Tin</strong>: Chúng tôi sử dụng thông tin khách hàng cho những mục đích sau:</p>

                    <p>- Cung cấp những thông tin hữu ích nhằm chăm sóc tốt nhất cho khách hàng, bao gồm: &nbsp;Bản tin hàng ngày (Newsletter), Các chương trình khuyến mãi, Thông tin về sản phẩm mới hoặc các chương trình thúc đẩy phát triển khác của công ty chúng tôi.</p>

                    <p>- Cung cấp một số tiện ích cũng như các dịch vụ tư vấn hỗ trợ khách hàng tốt hơn.</p>

                    <p>- Chúng tôi sẽ sử dụng thông tin khách hàng một cách hợp lý vì mục đích phát triển và hoàn thiện những dịch vụ của công ty để có thể phục vụ tốt hơn quý khách hàng.</p>

                    <p>- Giải quyết những vấn đề tranh chấp, khiếu nại, giải đáp thắc mắc của khách hàng.</p>

                    <p>- Phòng tránh những trường hợp đáng tiếc có thể xảy ra sau này như những hành vi vi phạm pháp luật.</p>

                    <p><strong>Chia Sẻ Thông Tin:</strong> Như đã nói, chúng tôi rất coi trọng việc bảo mật thông tin khách hàng nên chúng tôi cam kết sẽ tuyệt đối không tự ý sử dụng thông tin khách hàng với mục đích không mang lại lợi ích cho khách hàng, chúng tôi cam kết không buôn bán, trao đổi thông tin bảo mật của khách hàng cho bất cứ bên thứ ba nào. Tuy nhiên, trong một số trường hợp đặc biệt sau, chúng tôi có thể chia sẻ thông tin khách một cách hợp lý:</p>

                    <p>- Được sự đồng ý của khách hàng.</p>

                    <p>- Để bảo vệ quyền lợi của công ty và những đối tác của công ty: Chúng tôi chỉ đưa ra những thông tin cá nhân của khách hàng khi chắc chắn rằng những thông tin đó có thể bảo vệ được quyền lợi, tài sản của công ty chúng tôi và những đối tác liên quan. Những thông tin này sẽ được tiết lộ một cách hợp pháp theo Pháp luật Việt Nam.</p>

                    <p>- Theo yêu cầu của những cơ quan chính phủ khi chúng tôi thấy nó phù hợp với pháp luật Việt Nam.</p>

                    <p>- Trong một số trường hợp cần thiết phải cung cấp thông tin khách hàng khác, như các chương trình khuyến mãi có sự tài trợ của một bên thứ ba chẳng hạn, chúng tôi sẽ thông báo cho quý khách hàng trước khi thông tin của quý khách được chia sẻ. Qúy khách có quyền quyết định xem có đồng ý chia sẻ thông tin hoặc tham gia hay không.</p>

                    <h3>Bảo Mật Thông Tin Khách Hàng</h3>

                    <p>- Chúng tôi cam kết bảo đảm an toàn thông tin cho quý khách hàng khi đăng ký thông tin cá nhân với công ty chúng tôi. Chúng tôi cam kết không trao đổi mua bán thông tin khách hàng vì mục đích thương mại. Mọi sự chia sẻ và sử dụng thông tin khách hàng chúng tôi cam kết thực hiện theo chính sách bảo mật của công ty. Chúng tôi cam kết sẽ khiến quý khách cảm thấy tin tưởng và hài lòng về bảo mật thông tin cá nhân khi tham gia và sử dụng những dịch vụ của công ty chúng tôi.</p>

                    <p>- Để bảo mật thông tin của khách hàng tốt nhất, chúng tôi khuyến cáo quý khách hạn chế truy cập tài khoản bằng đăng nhập tự động, chú ý chế độ sao lưu password và đảm bảo đăng xuất khỏi tài khoản khi sử dụng máy tính chung để đăng nhập tài khoản trên website của chúng tôi. Chúng tôi sẽ không chịu trách nhiệm khi những thông tin cá nhân của quý khách bị rò rỉ vì những lý do trên.</p>

                    <p>- Chính sách bảo mật chỉ áp dụng những thông tin quý khách hàng đăng ký trên website chính thức thethaothientruong.vn của công ty chúng tôi. Mọi thông tin quý khách đăng ký tại những wedsite hoặc những địa chỉ khác đều không thuộc phạm vi hiệu lực của Chính sách Bảo mật thông tin này.</p>

                    <p>Chúng tôi luôn nhấn mạnh khẳng định rằng: Chúng tôi rất coi trọng quyền lợi được bảo mật thông tin của quý khách hàng. Nếu như quý khách có bất cứ thắc mắc hay góp ý nào liên quan đến Chính sách bảo mật của công ty, vui lòng liên hệ với chúng tôi:</p>

