                  <h2 class="intro-detail"></h2>
                  <div class="content-detail"><p>Thể thao Thiên Trường luôn mong muốn mang đến cho khách hàng sự hài lòng trước và sau khi mua hàng bằng những chính sách chăm sóc khách hàng chu đáo và tận tâm. Vì vậy, trong thời gian sử dụng sản phẩm, nếu gặp bất kì trục trặc nào do lỗi kỹ thuật, khách hàng có thể liên hệ trực tiếp Trung tâm bảo hành của hãng (thông tin ghi trên phiếu bảo hành) hoặc Trung tâm chăm sóc khách hàng của Thiên Trường để được giúp đỡ. &nbsp;</p>

                    <h3>Quy định về sản phẩm được bảo hành miễn phí.</h3>

                    <p>Những sản phẩm được bảo hành miễn phí khi đảm bảo các điều kiện sau:</p>

                    <p>- Sản phẩm còn thời hạn bảo hành và có đăng ký bảo hành (tính từ ngày được ghi trên hóa đơn chứng từ mua hàng).</p>

                    <p>- Thời hạn bảo hành được ghi trên phiếu bảo hành và theo quy định của từng hãng sản xuất đối với tất cả sự cố về mặt kỹ thuật.</p>

                    <p>- Sản phẩm bị hư hỏng do linh kiện trong máy hoặc do nhà sản xuất.</p>

                    <p>- Phải có phiếu bảo hành và tem bảo hành của hãng trên sản phẩm, phiếu xuất hàng của Thiên Trường.</p>

                    <p>- Số máy và tem niêm phong trên sản phẩm phải còn nguyên vẹn, không bị rách hoặc mất đi.</p>

                    <p>- Số máy trên sản phẩm, trên phiếu bảo hành và trên phiếu xuất hàng phải giống nhau.</p>

                    <h3>Quy định về sản phẩm không được bảo hành miễn phí</h3>

                    <p>- Sản phẩm đã quá thời hạn bảo hành ghi trên phiếu hoặc mất phiếu bảo hành.</p>

                    <p>- Sử dụng không đúng theo sách hướng dẫn, sử dụng không đúng điện áp ghi trên sản phẩm.</p>

                    <p>- Tem niêm phong bảo hành bị rách, vỡ, bị dán đè hoặc bị sửa đổi.</p>

                    <p>- Phiếu bảo hành không ghi rõ số serial và ngày mua hàng.</p>

                    <p>- Sản phẩm hư hỏng do tác động bên ngoài như rơi, vỡ, va đập, trầy xước, móp méo, ẩm ướt, hoen rỉ, chảy nước hoặc do hỏa hoạn, thiên tai, côn trùng xâm nhập.</p>

                    <p>- Vỏ máy và các loại phụ kiện kèm theo như dây nguồn, dây tín hiệu, nắn dòng, điều khiển từ xa, pin điều khiển, tai nghe, quạt trên thiết bị, hoặc thiết bị do quạt bị hỏng gây ra cháy nổ.</p>

                    <p>- Các phần mềm miễn phí kèm theo máy.</p>

                    <p>- Sản phẩm có dấu hiệu đã bị sửa chữa trước khi đem đến Thiên Trường hoặc hãng.&nbsp;</p>

                    <h3>Bảo trì, vệ sinh sản phẩm</h3>

                    <p>- Vệ sinh, lau chùi máy.</p>

                    <p>- Thời gian bảo trì, vệ sinh cho sản phẩm tùy thuộc vào sự thỏa thuận giữa Thiên Trường và khách hàng.</p>

                    <h3>&nbsp;Địa điểm bảo hành, bảo trì</h3>

                    <p>- Nếu trên phiếu giao hàng, phiếu bảo hành không ghi thỏa thuận địa chỉ của khách hàng đã đăng ký, thì tất cả sản phẩm đều được bảo hành tại các trung tâm bảo hành của hãng.</p>

                    <p>- Bảo hành không bao gồm chi phí vận chuyển và giao hàng. &nbsp;</p>

                    <p>- Liên hệ, thắc mắc, khiếu mại về vấn đề bảo hành, bảo trì.</p>

                    <p>Khách hàng mang sản phẩm đến Trung Tâm Bảo Hành của Thiên Trường tại các địa chỉ sau:</p>

                    <p>Chi tiết liên hệ: Công ty TNHH Thể thao Thiên Trường</p>

