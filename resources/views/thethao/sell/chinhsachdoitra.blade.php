                  <h1 class="news-name-detail">Chính sách bảo đổi trả hàng hóa</h1>
                  <div class="createdate">08/05/2014 01:26</div>
                  <h2 class="intro-detail"></h2>
                  <div class="content-detail"><p>Đối với quý Khách Hàng tại khu vực Hà Nội có thể trực tiếp liên hệ Đổi/Trả hàng tại văn phòng của thể thao Thiên Trường ở địa chỉ: Số 208D, Lê Trọng Tấn, Thanh Xuân, Hà Nội.</p>

                    <p>Quý khách có thể thực hiện đổi trả hàng miễn phí trong vòng 03 ngày kể từ ngày nhận hàng cho các sản phẩm đặt mua tại thethaothientruong.vn. Các sản phẩm thực hiện đổi trả phải là sản phẩm không có dấu hiệu đã qua sử dụng và còn nguyên tem, mác, nguyên đai kiện ban đầu (trừ trường hợp sản phẩm bị lỗi hoặc hư hại trong quá trình vận chuyển). Ngoài ra, để thực hiện đổi trả, khách hàng phải còn giữ hóa đơn mua hàng tại thethaothientruong.vn.</p>

                    <p>Quy trình đổi trả hàng được thực hiện khi kiện hàng được hoàn về tại kho hàng của thethaothientruong.vn. Chúng tôi sẽ thực hiện các bước tiếp theo để kiểm tra chất lượng sản phẩm và tiến hành đổi/trả hàng hoặc hoàn tiền theo yêu cầu của quý khách.</p>

                    <p>Một số điều kiện có thể đi kèm, quý khách vui lòng tham khảo thông tin chi tiết bên dưới:</p>

                    <h2>I. Các trường hợp được đổi trả hàng.</h2>

                    <p><strong>1. &nbsp;Đổi trả theo nhu cầu khách hàng (đổi trả hàng vì không ưng ý).</strong></p>

                    <p>Tất cả mặt hàng mua từ thethaothientruong.vn&nbsp;đều có thể đổi hoặc trả trong vòng 03 ngày kể từ ngày nhận hàng ( trừ khi có quy định gì khác). Chúng tôi chấp nhận đổi trả cho các sản phẩm còn nguyên điều kiện ban đầu, còn hóa đơn mua hàng tại thethaothientruong.vn &amp; sản phẩm chưa qua sử dụng, bao gồm các giới hạn sau:</p>

                    <p>- Còn nguyên đóng gói và bao bì không bị móp rách.</p>

                    <p>- Đầy đủ các bộ phận, chi tiết, phụ kiện.</p>

                    <p>- Tem/ phiếu bảo hành, tem thương hiệu, hướng dẫn kỹ thuật &nbsp;và các quà tặng kèm theo (nếu có)... phải còn đầy đủ và nguyên vẹn.</p>

                    <p>- Không bị dơ bẩn, trầy xước, bể vỡ, hư hỏng, có mùi lạ hoặc có dấu hiệu đã qua hoặc qua sử dụng.</p>

                    <p>Lưu ý: Những sản phẩm đổi trả không đáp ứng các điều kiện nêu trên sẽ được tự động chuyển hoàn về địa chỉ đăng ký theo thông tin đơn hàng của quý khách.</p>

                    <p><strong>2. Đổi trả không vì lý do chủ quan từ khách hàng.</strong></p>

                    <p>thethaothientruong.vn khuyến khích quý khách hàng phải kiểm tra tình trạng bên ngoài của thùng hàng và sản phẩm trước khi thanh toán để đảm bảo rằng hàng hóa được giao đúng chủng loại, số lượng, màu sắc theo đơn đặt hàng và tình trạng bên ngoài không bị tác động (bể vỡ/ trày xước). Nếu gặp trường hợp này, Quý khách vui lòng từ chối nhận hàng và/hoặc báo ngay cho chúng tôi để có phương án xử lí kịp thời. (Xin lưu ý những bước kiểm tra sâu hơn như dùng thử sản phẩm chỉ có thể được chấp nhận sau khi đơn hàng được thanh toán đầy đủ).</p>

                    <p>Trong trường hợp khách hàng đã thanh toán, nhận hàng và sau đó phát hiện hàng hóa bị bể vỡ, sai nội dung hoặc thiếu hàng, xin vui lòng chụp ảnh sản phẩm gửi về Email của chúng tôi để được chúng tôi hỗ trợ các bước tiếp theo như đổi/trả hàng hoặc gửi sản phẩm còn thiếu đến quý khách...</p>

                    <p>Sau 48h kể từ ngày quý khách nhận hàng, thethaothientruong.vn có quyền từ chối hỗ trợ cho những khiếu nại theo nội dung như trên.</p>

                    <p><strong>3. Hàng giao bị lỗi kĩ thuật</strong></p>

                    <p>Sản phẩm chỉ được xem là lỗi kĩ thuật khi được xác nhận từ chúng tôi theo số điện thoại hỗ trợ khách hàng : 09686. 50686.</p>

                    <p>Khi quý khách gặp trục trặc với sản phẩm đặt mua tại thethaothientruong.vn, Xin vui lòng thực hiện các bước sau đây:</p>

                    <p><strong>Bước 1: </strong>Tự kiểm tra cách thức sử dụng sản phẩm, các thao tác được hướng dẫn trong “Sách hướng dẫn sử dụng” đi kèm với mỗi sản phẩm (nếu có).</p>

                    <p><strong>Bước 2:</strong> Trong vòng 03 ngày kể từ ngày nhận hàng, nếu quý khách được xác nhận từ &nbsp;chúng tôi rằng sản phẩm bị lỗi kỹ thuật, quý khách vui lòng truy cập ngay Hướng dẫn đổi trả hàng để bắt đầu quy trình đổi trả hàng.</p>

                    <p>Theo kinh nghiệm của chúng tôi, hơn 80% sản phẩm hoàn về vì lí do lỗi kỹ thuật sau khi được bộ phận kĩ thuật kiểm tra lại, đều đánh giá sản phẩm nằm trong điều kiện sử dụng hoàn hảo. Vì vậy, để tiết kiệm thời gian và thuận tiện cho quý khách, vui lòng đọc kĩ hướng dẫn sử dụng hoặc liên hệ tham vấn trực tiếp với chúng tôi theo số điện thoại: 09686 50686 để chắc rằng sản phẩm đã được lắp ráp, kết nối và vận hành chính xác trước khi gửi hàng về lại thethaothientruong.vn</p>

                    <h2>II. Phương thức hoàn tiền.</h2>

                    <p>Tùy theo lý do hoàn trả sản phẩm kết quả đánh giá chất lượng tại kho, thethaothientruong.vn sẽ có những phương thức hoàn tiền với chi tiết như sau:</p>

                    <h3>Lý do đổi trả hàng.</h3>

                    <p>Phương thức hoàn tiền.</p>

                    <p>- Đổi trả theo nhu cầu khách hàng (đổi trả hàng vì không ưng ý với sản phẩm đã chọn) - Quý khách phải chịu tiền phí vận chuyển</p>

                    <p>- Đổi trả không vì lý do chủ quan từ khách hàng - Đổi sản phẩm mới cùng loại.</p>

                    <p>- Chuyển khoản qua ngân hàng theo thông tin của quý khách cung cấp.</p>

                    <p>- Riêng đối với các đơn hàng thanh toán qua thẻ ATM, chúng tôi sẽ áp dụng hình thức hoàn tiền vào tài khoản thanh toán của chủ thẻ.</p>

                    <p>Mọi chi tiết hoặc thắc mắc quý khách vui lòng liên hệ với chúng tôi qua số điện thoại hỗ trợ hoặc Email: thethaothientruong@ttsport.vn.&nbsp;Xin chân thành cảm ơn.</p>

                    <p>Với tiêu chí 3 nhất: Sản phẩm chất lượng nhất - Giá thành tốt nhất - Chăm sóc khách hàng nhiệt tình &amp; chu đáo nhất.</p>

                    <p>Mọi thông tin thắc mắc quý khách vui lòng liên hệ số hotline&nbsp;0968650686 !</p>
